function flattenExceptLastLevel($array, $prefix = '')
{
    $languageKeys = ['en', 'hr', 'de', 'tr']; // Define the language keys
    $result = [];
    foreach ($array as $key => $value) {
        if (is_array($value)) {
            // Check if the array contains translation data by looking for language keys
            if (array_intersect_key(array_flip($languageKeys), $value)) {
                $result[$prefix . $key] = $value;
            } else {
                // Recursively flatten the array if it doesn't contain translation data
                $result = array_merge($result, flattenExceptLastLevel($value, $prefix . $key . '.'));
            }
        } else {
            $result[$prefix . $key] = $value;
        }
    }
    return $result;
}

